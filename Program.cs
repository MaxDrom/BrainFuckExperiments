machine.RegisterCommand('>',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        position++;
        bufferPosition++;
    });
machine.RegisterCommand('<',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        position++;
        bufferPosition--;
    });
machine.RegisterCommand('+',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        position++;
        value++;
    });
machine.RegisterCommand('-',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        position++;
        value--;
    });
machine.RegisterCommand('.',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        position++;
        Console.Write((char)value);
    });
machine.RegisterCommand('[',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        if (value != 0)
        {
            position++;
            return;
        }

        position = brackets[position] + 1;
    });
machine.RegisterCommand(']',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        if (value == 0)
        {
            position++;
            return;
        }

        position = brackets[position];
    });
machine.RegisterCommand(',',
    (ref int position,
        ref int bufferPosition,
        ref byte value,
        Dictionary<int, int> brackets) =>
    {
        position++;
        value = (byte)Console.Read();
    });


